landscape = tkinter.Tk() # Creates a windo to mesure the landscape with.
landscape.withdraw() # Hides the window.

x = landscape.winfo_screenwidth() # Gets width
y = landscape.winfo_screenheight() # Gets height

scale = min(x/1400, y/305) # Uses the smallest value as referencia to make sure not to draw out of boundaries. 

t.setup(x, y, 0, 0) # (width and height)

landscape.destroy() # Closes tkinter's bubble text

t.speed(0) # Defines the turtl's speed
