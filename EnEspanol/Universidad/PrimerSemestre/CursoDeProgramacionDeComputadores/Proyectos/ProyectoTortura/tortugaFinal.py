t.title("Parcial Turtle Equipo 3")  # Define el titulo del programa

def move(x, y): # Mueve a la tortuga para poder imprimir la siguiente letra teniendo en cuenta la escala
    x *= escala
    y *= escala
    t.setheading(0)
    t.up()
    t.goto(x, y)
    t.down()

